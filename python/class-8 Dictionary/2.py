#dictionary
course={"course_name":"python","duration":"3 months","trainer":"sai","location":"hyd"}
#change the course name 
course["course_name"]="java"
#change the duration 
course["duration"]="1 month"
#change the trainer 
course["trainer"]="abc"
#change the location 
course["location"]="islamabad"

#to access the data inside course dictionary, we use key names
print(course["course_name"])
print(course["duration"])
print(course["trainer"])
print(course["location"])


